public class TernaryOperatorUsage {
    public static void main(String[] args) {
        // Syntax: result = condition ? value_if_true : value_if_false;
        // Example usage to simplify code:
        int number = 10;
        String message = number > 5 ? "Number is greater than 5" : "Number is 5 or smaller";
        System.out.println(message);
    }
}